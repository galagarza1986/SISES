    public void setTable(String[][] matriz) {
        this.jScrollPane1.setViewportView(this.tblDataStudent);
        DefaultTableModel modelo = new DefaultTableModel(matriz, new String[]{"Carnet", "Nombre", "Edad", "Teléfono", "Dirección"});
        this.tblDataStudent.setModel(modelo);
        this.tblDataStudent.setAutoCreateRowSorter(true);
        this.sorter = new TableRowSorter<>(modelo);
        this.tblDataStudent.setRowSorter(sorter);
    }
    
    public void listener(MouseListener controller){
        this.tblDataStudent.addMouseListener(controller);
    }
    
    public String[] getRow(){
        String[] dataRow=new String[5];
        int selectedRow=this.tblDataStudent.getSelectedRow();
        for (int i = 0; i < this.tblDataStudent.getColumnCount(); i++) {
            dataRow[i]=String.valueOf(this.tblDataStudent.getValueAt(selectedRow, i));
        }
        return dataRow;
    }


