    private void txtFiltrarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltrarKeyReleased
        // TODO add your handling code here:
        try {
            if (this.cboxColumna.getSelectedItem().equals("Todas")) {
                this.sorter.setRowFilter(RowFilter.regexFilter("(?i)" + this.txtFiltrar.getText()));
            } else if (this.cboxColumna.getSelectedItem().equals("Carnet")) {
                this.sorter.setRowFilter(RowFilter.regexFilter("(?i)" + this.txtFiltrar.getText(), 0));
            } else if (this.cboxColumna.getSelectedItem().equals("Nombre")) {
                this.sorter.setRowFilter(RowFilter.regexFilter("(?i)" + this.txtFiltrar.getText(), 1));
            } else {
                this.sorter.setRowFilter(RowFilter.regexFilter("(?i)" + this.txtFiltrar.getText(), 2));
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtFiltrarKeyReleased


